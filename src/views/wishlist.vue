    components: {
        Header,
        Footer
    },
    data() {
        return {
            Allproducts: [],
            loading: true,
            NoRecord: false,
            AllproductsShow: false,
            name: this.$route.params.name,
            uid: localStorage.getItem("user"),
        };
    },
    mounted() {
        this.getData();
        this.goToTop()
    },
    watch: {
        Allproducts: function (id) {
            this.Allproducts = id;
            this.getData();
        },
    },
    methods: {
        goToTop() {
            document.body.scrollTop = 0
            document.documentElement.scrollTop = 0
        },
        getData() {
            const axios = require("axios");
            var ip_address = JSON.parse(localStorage.getItem("ip_address"));
            var user_session = JSON.parse(localStorage.getItem("user"));
            var uid = "";
            if (user_session != null) {
                uid = user_session;
            } else {
                uid = null;
            }
            var self = this;
            axios
                .post(this.$root.URL_ROOT + "api.php?type=whishlistProd", {
                    uid: uid,
                    ip_address: ip_address,
                })
                .then(function (response) {
                    var data = response.data;
                    if (data.status == 'success') {
                        self.loading = false
                        self.AllproductsShow = true
                        self.Allproducts = data.data;
                        console.log(response);
                    } else {
                        //alert('hello')
                        self.loading = false
                        self.NoRecord = true
                        self.AllproductsShow = false
                    }
                });
        },
    },
};
