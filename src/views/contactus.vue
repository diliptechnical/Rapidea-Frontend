import axios from "axios";
import $ from "jquery";
import Header from "@/components/navbar";
import Footer from "@/components/footer";
export default {
    components: {
        Header,
        Footer
    },
    data() {
        return {
            fields: {
                type: "update_profile",
                uid: "",
                fullname: "",
                mobile: "",
                address: "",
                Token: this.$root.auth,
            },
            name: '',
            mobile: '',
            msg: '',
            email: '',
            femail: '',
            msg2: [],
            msgs: [],
            e_name: '',
            e_mobile: ''
        };
    },
    mounted() {
        this.uid = localStorage.getItem("user");

    },
    watch: {
        femail(value) {
            // binding this to the data value in the email input
            this.femail = value;
            this.validateEmail2(value);
        },
        name(value) {
            // binding this to the data value in the email input
            this.name = value;
            this.validateUsername(value);
        },
        mobile(value) {
            // binding this to the data value in the email input
            this.mobile = value;
            this.validateMobile(value);
        },
    },
    methods: {
        validateEmail2(value) {
            if (/^\w+([-]?\w+)@\w+([-]?\w+)(\.\w{2,3})+$/.test(value)) {
                this.msg2['email'] = '';

            } else {
                this.msg2['email'] = 'Invalid Email Address';

            }

        },
        validateUsername(value) {
            if (value == null) {
                this.msgs['e_name'] = 'Please Enter Your Name.';
                this.check_val = true
            } else if (value.length < 5) {
                this.msgs['e_name'] = 'Lenth must be greater then 5';
                this.check_val = true
            } else {
                this.msgs['e_name'] = ''
                this.check_val = false
            }

        },
        validateMobile(value) {
            if (value == null) {
                this.msgs['e_mobile'] = 'Please Enter Mobile no.';
                this.check_val = true
            } else if (value.length != 10 || value.length > 10 || value.length < 10) {
                this.msgs['e_mobile'] = 'Invalid Mobile no.';
                this.check_val = true
            } else {
                this.msgs['e_mobile'] = ''
                this.check_val = false
            }

        },
        quickbox() {
            if ($(window).width() > 767) {
                $(".quickview-button").magnificPopup({
                    type: "iframe",
                    delegate: "a",
                    preloader: true,
                    tLoading: "Loading image #%curr%...",
                });
            }
        },
        contact_submit() {
            var vi = this;

            if (this.msg == "") {
                this.$toasted.global.error({
                    message: "Please enter Message"
                });
            } else if (this.name == "") {
                this.$toasted.global.error({
                    message: "Please enter name"
                });
            } else if (this.femail == "") {
                this.$toasted.global.error({
                    message: "Please enter email"
                });
            } else if (this.mobile == "") {
                this.$toasted.global.error({
                    message: "Please enter mobile number"
                });
            } else {
                axios({
                    method: "POST",
                    url: this.$root.URL_ROOT + "api.php",
                    data: {
                        type: "contact",
                        name: this.name,
                        email: this.femail,
                        mobile: this.mobile,
                        msg: this.msg,
                    },
                }).then(function (response) {
                    var data = response.data;
                    console.log(response.data)
                    if (data.status == "success") {
                        vi.$toasted.global.success({
                            message: data.msg
                        });
                        vi.name = "";
                        vi.email = "";
                        vi.mobile = "";
                    } else {
                        vi.$toasted.global.error({
                            message: data.msg
                        });
                    }
                });
            }
        },
    },
};
